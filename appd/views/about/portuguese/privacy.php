<h2>Coleta e Utilização de Informação</h2>

<p><strong>Informação Coletada no Ato de Cadastro:</strong>
    Quando você criar ou reconfigurar sua conta no showthatyouhelp.com, você fornecerá algumas informações pessoais,
    como seu nome, nome de usuário, senha e endereço de e-mail. Alguns dados, por exemplo seu nome e nome de usuário,
    são listados publicamente no nossos Serviços, inclusive em sua página de perfil e nos resultados de buscas.
    Alguns Serviços, como a busca, os perfis de usuários públicos e listas de usuários, não necessitam cadastro.</p>

<p><strong>Informações Adicionais:</strong>
    Você pode fornecer outras informações de perfil para serem tornadas públicas, como um parágrafo curto sobre você,
    seu website ou uma foto. Nós poderemos utilizar sua informação de contato para lhe enviar informações sobre nossos Serviços.
    Você pode usar as configurações da sua conta para cancelar o recebimento de notificações do showthayouhelp.com. Você também pode
    cancelar o recebimento seguindo as instruções contidas na própria notificação. Nós poderemos utilizar
    suas informações de contato para ajudar outros a encontrar sua conta, inclusive através de serviços de terceiros.
    Se você entrar em contato através de email, nós poderemos arquivar sua mensagem, email e informações de contato
    para podermos responder a sua mensagem. Fornecer as informações adicionais definidas nessa seção é opcional.</p>

<p><strong>Atualizações, Listas e outras Informações Públicas:</strong>
    Nossos Serviços foram desenvolvidos para auxiliar você a divulgar informações com o resto do mundo. A maior parte da
    informação que você fornecer será dispoibilizada publicamente. Isso não inclui apenas
    os comentários que você compartilha e os metadados fornecidos com as Atualizações, mas também as listas que você cria,
    as pessoas que você recebe atualizações, as Atualizações que você comenta, e outras informações que resultam
    da sua utilização do Serviço. Por padrão, quase sempre tornaremos a informação que você fornece
    pública, até o momento em que você remover essa informação do showthatyouhelp.com.
    <?php //, but we generally give you settings to make the information more private if you want ?>
    Sua informação pública é disseminada ampla e instantâneamente.
    Por exemplo, suas informações do seu perfil de usuário público e suas atualizações podem ser buscadas
