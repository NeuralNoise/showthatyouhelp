    videos, e links através dos nossos Serviços, você deve tomar cuidado sobre o quê você está tornando público.</p>

<p><strong>Cookies:</strong>
    Como muitos websites, nós utilizamos a tecnologia de "cookies" para coletar dados de utilização adicionais
    e para melhorar nossos Serviços, mas nós não requeremos "cookies" para muitas partes do nosso Serviço como a busca
    ou acessar os perfis de usuário e as listas de usuários. Um "cookie" é um pequeno arquivo que é
    transferido para seu computador. showthatyouhelp.com pode utilizar tanto "cookies" de sessão e
    "cookies" persistentes para melhore entender como você interage com nossos Serviços, para monitorar
    a utilização por parte de nossos usuários e direcionar o tráfico web nos nossos Serviços, e para customizar e
    melhorar nossos Serviços. A maioria dos navegadores de Internet aceitam "cookies" automiaticamente. Você pode instruir
    seu navegador, mudando suas configurações, a não aceitar "cookies" ou lhe avisar antes de aceitar
    um "cookie" de um website que você visitar. Contudo, alguns Serviçoes podem não funcionar perfeitamente se você
    desabilitar os "cookies".</p>

<p><strong>Serviços de Terceiros:</strong>
    O showthatyouhelp.com utiliza uma variedade de serviços de terceiros para nos auxiliar a oferecer nossos Serviços,
    tais como o Google Analytics e Google Adsense. Esses provedores de serviço podem
    coletar informações que seu navegador enviar como parte da requisição da página web, como "cookies"
    ou seu endereço IP.</p>

<h2>Divulgação e Compartilhamento de Informações</h2>

<p><strong>Terceiros e Provedores de Serviço:</strong>
    Nós não vendemos, trocamos ou transferimos suas informações pessoais para terceiros.
    Isso não inclui provedores de serviço em quem confiamos e que no auxiliam na operação
    do nosso website, na condução do nosso negócio, ou servindo você. Nós podemos compartilhar
    suas informações pessoais privadas com esses provedores de serviço de acordo com as obrigações
    contidas nessa Política de Privacidade, e na condição de que esses provedores de serviço só utilizem
    seus dados pessoais em nosso no e de acordo com nossas instruções.</p>

<p><strong>Lei e Dano:</strong>
    Sem prejuízo de disposição em contrário nesta Política de Privacidade, nós podemos preservar ou divulgar suas
    informações se acreditarmos que é razoavelmente necessário para o cumprimento de obrigações legais, regularórias
    ou pedidos efetuados nos termos da lei; para proteger a segurança de qualquer pessoa; para resolver problemas de fraude, segurança, o
    técnicos; ou para proteger os direitos e propriedade do showthatyouhelp.com e da Softerize Sistemas Ltda. Contudo,
    nada nesta Política de Privacidade tem a intenção de limitar quaisquer direitos de defesa ou oposição que lhe assistam em relação
    a pedidos de divulgação da sua informação efetuados por terceiros, incluindo quaisquer entidades governamentais.</p>

<p><strong>Transferência do Negócio:</strong>
    No caso de o showthatyouhelp.com ou a Softerize Sistemas Ltda. estar envolvido em provesso de insolvência, recuperação, fusão,
    aquisição, reorganização ou venda de ativos, sua informação poderá ser vendida ou transferida
    como parte da transação. As previsões constantes nessa Política de Privacidade também serão aplicadas
    a sua informação após a transferência para a nova entidade.</p>

<p><strong>Informação Não Privada Nem Pessoal:</strong>
    Nós podemos compartilhar ou divulgar sua informação que não seja privada ou pessoal,
    como suas informações de perfil públicas, Atualizações, listas de pessoas que recebem suas atualizações
    e lista de pessoas das quais você recebe atualizações.</p>

