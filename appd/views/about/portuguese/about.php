<p>A ideia para este aplicativo apareceu em 2009, quando <?php echo anchor(base_url('p/oscardias'), 'Oscar Dias'); ?> fundou a <a href="http://www.softerize.com.br" title="Softerize">Softerize</a>.
Devido a diversas razões, esse projeto foi adiado diversas vezes, sendo finalizado apenas em 2012. Seu objetivo é ser um centro para pessoas e organizações
compartilharem informações sobre sustentabilidade, como notícias, iniciativas, fotos, artigos, etc.</p>

<p>Apesar de o aquecimento global ser um assunto comum em canais de TV e revistas, a discussão sobre sustentailidade vai além disso.
A população e o consumo crescentes no nosso planeta, levam a desafios econômicos, ambientais e sociais.</p>

