<h2>Motivação</h2>

<div id="population-chart" style="height:300px; width:710px;"></div>

<p><small>Fonte: Population Division of the Department of Economic and Social Affairs of the United Nations Secretariat, <a href="http://esa.un.org/unpd/wpp/index.htm">World Population Prospects: The 2010 Revision</a> e
    <a href="http://www.un.org/esa/population/publications/sixbillion/sixbillion.htm">The World at Six Billion</a></small></p>

<p>Esse gráfico resume a maior motivação para a criação deste aplicativo. Em apenas 50 anos (1960 - 2010) a população do nosso planeta dobrou.
Quando consideramos o fato de que nosso planeta possui alguns recursos que são limitados, é lógico concluirmos que precisamos balancear
nosso estilo de vida para não acabarmos com esses recursos e destruirmos nosso planeta.</p>

<p>O crescimento populacional tem implicações na nossa sociedade, economia e meio ambiente. Muitos cientistas afirmam que somos a maior causa
para as mudanças climáticas, devido ao aumento da emissão de gases que causam o efeito estufa. A incessante busca pelo crescimento econômico
está nos levando a isso. Diversos animais foram extintos ou estão ameaçados devido a destruição de seus habitates naturais.</p>

