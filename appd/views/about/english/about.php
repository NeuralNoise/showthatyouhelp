<h2>Motivation</h2>

<div id="population-chart" style="height:300px; width:710px;"></div>

<p><small>Source: Population Division of the Department of Economic and Social Affairs of the United Nations Secretariat, <a href="http://esa.un.org/unpd/wpp/index.htm">World Population Prospects: The 2010 Revision</a> and
    <a href="http://www.un.org/esa/population/publications/sixbillion/sixbillion.htm">The World at Six Billion</a></small></p>

<p>This chart sums up the main motivation for this web app. In 50 years (1960 - 2010) our population more than doubled.
When you consider the fact that our planet has some resources that are limited, it seems logic that we need to "calibrate"
our way of life in order to maintain our world function for as long as we can.</p>

<p>The population growth has implications in our society, economy and, specially, in the environment. Many scientists claim that we are the
main cause for the climate change, due to the increase emission of greenhouse gases. There are also many endangered species and even extinct
animals because we are destroying their natural habitats.</p>

